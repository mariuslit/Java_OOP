package k_1_31_inheritance_figuros;

public class Staciakampis extends Figuros {

    private Double krastineX;
    private Double krastineY;

    public Staciakampis(Double krastineX, Double krastineY) {
        this.krastineX = krastineX;
        this.krastineY = krastineY;
    }

    public Double getKrastineX() {
        return krastineX;
    }

    public void setKrastineX(Double krastineX) {
        this.krastineX = krastineX;
    }

    public Double getKrastineY() {
        return krastineY;
    }

    public void setKrastineY(Double krastineY) {
        this.krastineY = krastineY;
    }
}
